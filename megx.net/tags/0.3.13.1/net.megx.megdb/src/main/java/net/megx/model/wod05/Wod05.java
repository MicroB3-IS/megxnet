package net.megx.model.wod05;

public class Wod05 {
	private Integer year;
	private Integer month;
	private Double temperature;
	private Double salinity;
	private Double oxygen;
	private Double phosphate;
	private Double silicate;
	private Double nitrate;
	private Double nitrite;
	private Double ph;
	private Double chlorophyll;
	private Double planktonBiomass;
	private Double alkalinity;
	
	public Integer getYear() {
		return year;
	}
	public void setYear(Integer year) {
		this.year = year;
	}
	public Integer getMonth() {
		return month;
	}
	public void setMonth(Integer month) {
		this.month = month;
	}
	public Double getTemperature() {
		return temperature;
	}
	public void setTemperature(Double temperature) {
		this.temperature = temperature;
	}
	public Double getSalinity() {
		return salinity;
	}
	public void setSalinity(Double salinity) {
		this.salinity = salinity;
	}
	public Double getOxygen() {
		return oxygen;
	}
	public void setOxygen(Double oxygen) {
		this.oxygen = oxygen;
	}
	public Double getPhosphate() {
		return phosphate;
	}
	public void setPhosphate(Double phosphate) {
		this.phosphate = phosphate;
	}
	public Double getSilicate() {
		return silicate;
	}
	public void setSilicate(Double silicate) {
		this.silicate = silicate;
	}
	public Double getNitrate() {
		return nitrate;
	}
	public void setNitrate(Double nitrate) {
		this.nitrate = nitrate;
	}
	public Double getNitrite() {
		return nitrite;
	}
	public void setNitrite(Double nitrite) {
		this.nitrite = nitrite;
	}
	public Double getPh() {
		return ph;
	}
	public void setPh(Double ph) {
		this.ph = ph;
	}
	public Double getChlorophyll() {
		return chlorophyll;
	}
	public void setChlorophyll(Double chlorophyll) {
		this.chlorophyll = chlorophyll;
	}
	public Double getPlanktonBiomass() {
		return planktonBiomass;
	}
	public void setPlanktonBiomass(Double planktonBiomass) {
		this.planktonBiomass = planktonBiomass;
	}
	public Double getAlkalinity() {
		return alkalinity;
	}
	public void setAlkalinity(Double alkalinity) {
		this.alkalinity = alkalinity;
	}
}

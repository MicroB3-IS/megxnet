package net.megx.model.browse;

public class SamplingSitesRow {
	private String samplingSite;
	private String latlon;
	private String numSamples;
	public String getSamplingSite() {
		return samplingSite;
	}
	public void setSamplingSite(String samplingSite) {
		this.samplingSite = samplingSite;
	}
	public String getLatlon() {
		return latlon;
	}
	public void setLatlon(String latlon) {
		this.latlon = latlon;
	}
	public String getNumSamples() {
		return numSamples;
	}
	public void setNumSamples(String numSamples) {
		this.numSamples = numSamples;
	}
}
